package com.binaryOperator;

public class Example2 
{
	/*
	 *Relational Opretors:
	 *
	 * Note: these all Opretors generate result in the form of 
	 *     booean values (true/false)
	 *     
	 */
	
	public static void main(String[] args)
	
{
		int a = 500;
		int b = 20;
		
		System.out.println(">: "+(a>b));
		System.out.println("<: "+(a<b));
		System.out.println(">=: "+(a>=b));
		System.out.println("<=: "+(a<=b));
		System.out.println("==: "+(a==b));
		System.out.println("!=: "+(a!=b));
		

	

	}

}
