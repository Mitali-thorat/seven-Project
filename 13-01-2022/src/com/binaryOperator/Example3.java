package com.binaryOperator;

public class Example3
    {

	public static void main(String[] args)
	{
		
		
		int a = 140;
		int b = 14;
		
		System.out.println("+=: "+(a+=b));
		System.out.println("-=: "+(a-=b));
		System.out.println("*=: "+(a*=b));
		System.out.println("/=: "+(a/=b));
		System.out.println("%=: "+(a%=b));
		

	
	}

}
