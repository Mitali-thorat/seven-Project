package com.binaryOperator;

public class Example1
{


	// Arithmatic Operators
	
	public static void main(String[] args)
	{

		
		int a = 400;
		int b = 20;
		
		System.out.println("+: "+(a+b));
		System.out.println("-: "+(a-b));
		System.out.println("*: "+(a*b));
		System.out.println("/: "+(a/b));
		System.out.println("%: "+(a%b));
		
		

	}

}
